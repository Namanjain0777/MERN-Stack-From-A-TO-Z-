// switch case

let val = 6;
switch(val){
    case 1:
        console.log("Values is..... 1");
        break;
    case 2:
        console.log("Values is..... 2");
        break;
    case 3:
        console.log("Values is..... 3");
        break;
    default:
        console.log("Values is...I cant guess ERROR....");
}