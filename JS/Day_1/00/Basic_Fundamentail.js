// JavaScript was originally named Mocha.
// It was created in 1995 by Brendan Eich at Netscape.
// The name changed from Mocha → LiveScript → JavaScript.
// JavaScript has NOTHING to do with Java (name was just marketing).

// JavaScript is a Scripting Language.
// It is interpreted (not compiled like C/C++).
// It runs line by line and is mainly used to make web pages interactive.
// Today, JavaScript also runs on servers (Node.js), mobile apps, and more.

// ES5 is a Version of JavaScript.
// ES stands for ECMAScript.
// ECMAScript is the official standard/specification for JavaScript.
// Browsers implement JavaScript based on ECMAScript rules.

// ES5 was released in 2009.
// It was the most stable and widely supported version for many years.
// Older browsers (like Internet Explorer) mainly support ES5.

// Key features of ES5:
 // - var keyword for variables
 // - function keyword for functions
 // - Arrays and Objects
 // - for, while loops
 // - if-else, switch
 // - Basic string and number operations

//  Word vs Keywords
// Word:
// A word in JavaScript is any normal name or text that does NOT have
// special meaning to the JavaScript engine.

// A keyword is a reserved word in JavaScript.
// Keywords have a predefined meaning and purpose.
// You CANNOT use keywords as variable or function names.